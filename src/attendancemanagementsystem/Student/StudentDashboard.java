    private void submitUpdateButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitUpdateButtonMouseClicked
        if (studentDatabase.getStudentPass().equals(studentConfirmPassField.getText())) {
            String studentPass;
            confirmPassLabel.setVisible(false);
            if (!studentNewPassField.getText().equals("")) {
                studentPass = studentNewPassField.getText();
            } else {
                studentPass = studentDatabase.getStudentPass();
            }
            boolean updateDone = studentDatabase.updateProfile(studentPass, studentNameTextField.getText(), studentEmailTextField.getText(), studentPhoneTextField.getText(), studentAddress.getText(), studentBloodGroup.getText());
            if (updateDone) {
                JOptionPane.showMessageDialog(panel, "Your profile has been updated successfully", "Successful Update", JOptionPane.INFORMATION_MESSAGE);
                updateProfileButton.setVisible(true);
                submitUpdateButton.setVisible(false);
                getStudentProfile();
            }
        } else {
            confirmPassLabel.setVisible(true);
        }
    }//GEN-LAST:event_submitUpdateButtonMouseClicked

