    private void getAttendance(int month) {
        boolean[] attendance = studentDatabase.getAttendance(month);
        DefaultTableModel dtm = (DefaultTableModel) attendanceTable.getModel();
        dtm.setRowCount(0);
        Object[] row = new Object[2];
        for (int i = 1; i <= 30; i++) {
            row[0] = "Day " + (i);
            if (attendance[i] == true) {
                row[1] = "Present";
            } else {
                row[1] = "Absent";
            }
            dtm.addRow(row);
        }
    }

